package src;

public class Turees {
    private Mashin mashin;
    private Uilchluulegch uilchluulegch;
    private int honog;

    public Turees(Mashin mashin, Uilchluulegch uilchluulegch, int honog) {
        this.mashin = mashin;
        this.uilchluulegch = uilchluulegch;
        this.honog = honog;
    }

    public Mashin getMashin() {
        return mashin;
    }

    public Uilchluulegch getUilchluulegch() {
        return uilchluulegch;
    }

    public int getHonog() {
        return honog;
    }
}
